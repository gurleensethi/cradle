package config

import (
	"errors"
	"fmt"
	"os"
	"path"
	"slices"
	"strings"

	"github.com/gurleensethi/cradle/internal/types"
	"gopkg.in/yaml.v3"
)

const (
	EnvCradleHome        = "CRADLE_HOME"
	CradleConfigFileName = "cradle.yaml"

	CradleConfigFileHeader = `# Code generated by cradle. DO NOT EDIT.`
)

// cradleConfig is used for YAML marshaling
type cradleConfig struct {
	Projects []types.CradleProject `yaml:"projects"`
}

// Config holds the cradle configuration.
type Config struct {
	CradleHomeDirPath    string
	CradleConfigFilePath string
	CradleCommandOut     bool
	projects             []types.CradleProject
}

var instance Config

// Get returns a copy of the configuration.
func Get() Config {
	return instance
}

// Init initializes the cradle configuration.
func Init() error {
	instance = Config{}

	instance.CradleCommandOut = slices.Contains(
		[]string{"1", "true", "TRUE", "True"},
		strings.TrimSpace(os.Getenv("CRADLE_CMDOUT")),
	)

	cradleHomePath, err := getCradleHomeDir()
	if err != nil {
		return err
	}

	err = ensureCradleHomeDir(cradleHomePath)
	if err != nil {
		return err
	}

	cradleConfigFilePath, err := ensureCradleConfigFile(cradleHomePath)
	if err != nil {
		return err
	}

	projects, err := parseCradleConfigFile(cradleConfigFilePath)
	if err != nil {
		return err
	}

	instance.CradleHomeDirPath = cradleHomePath
	instance.CradleConfigFilePath = cradleConfigFilePath
	instance.projects = projects

	return nil
}

// Projects returns a copy of the projects list.
func Projects() []types.CradleProject {
	projects := make([]types.CradleProject, len(instance.projects))
	copy(projects, instance.projects)
	return projects
}

// AddProject adds a project to the configuration and saves it.
func AddProject(project types.CradleProject) error {
	instance.projects = append(instance.projects, project)
	return save()
}

// RemoveProjectByName removes a project by name or path and saves the configuration.
func RemoveProjectByName(name string) error {
	for i, project := range instance.projects {
		if project.MatchPathOrName(name) {
			instance.projects = append(instance.projects[:i], instance.projects[i+1:]...)
			return save()
		}
	}
	return fmt.Errorf("project not found")
}

// UpdateProjects updates all projects and saves the configuration.
func UpdateProjects(projects []types.CradleProject) error {
	instance.projects = projects
	return save()
}

// ForEachProject iterates over all projects.
func ForEachProject(fn func(types.CradleProject) bool) {
	for _, project := range instance.projects {
		if !fn(project) {
			return
		}
	}
}

// FindProject finds a project by query (path or name).
func FindProject(query string) (types.CradleProject, bool) {
	for _, project := range instance.projects {
		if project.MatchPathOrName(query) {
			return project, true
		}
	}
	return types.CradleProject{}, false
}

// TemporaryProjects returns all temporary projects.
func TemporaryProjects() []types.CradleProject {
	var temp []types.CradleProject
	for _, project := range instance.projects {
		if project.Temporary {
			temp = append(temp, project)
		}
	}
	return temp
}

// PermanentProjects returns all non-temporary projects.
func PermanentProjects() []types.CradleProject {
	var permanent []types.CradleProject
	for _, project := range instance.projects {
		if !project.Temporary {
			permanent = append(permanent, project)
		}
	}
	return permanent
}

func save() error {
	slices.SortFunc(instance.projects, func(a, b types.CradleProject) int {
		return strings.Compare(a.Path, b.Path)
	})

	fileBytes, err := yaml.Marshal(cradleConfig{Projects: instance.projects})
	if err != nil {
		return err
	}

	fileBytes = append([]byte(CradleConfigFileHeader+"\n\n"), fileBytes...)

	return os.WriteFile(instance.CradleConfigFilePath, fileBytes, 0666)
}

func getCradleHomeDir() (string, error) {
	cradleHomePath := strings.TrimSpace(os.Getenv(EnvCradleHome))
	if cradleHomePath == "" {
		userHomeDir, err := os.UserHomeDir()
		if err != nil {
			return "", err
		}

		cradleHomePath = path.Join(userHomeDir, "cradle")
	}

	return cradleHomePath, nil
}

func ensureCradleHomeDir(dirPath string) error {
	dirStat, err := os.Stat(dirPath)
	if err != nil {
		if errors.Is(err, os.ErrNotExist) {
			return os.MkdirAll(dirPath, os.ModePerm)
		}
		return err
	}

	if dirStat.IsDir() {
		return nil
	}

	return fmt.Errorf("%s is a file, either delete the file or change cradle home path by setting `CRADLE_HOME` env to something else", dirPath)
}

func ensureCradleConfigFile(dirPath string) (string, error) {
	configFilePath := path.Join(dirPath, CradleConfigFileName)

	configFileStat, err := os.Stat(configFilePath)
	if err != nil {
		if errors.Is(err, os.ErrNotExist) {
			configFile, err := os.Create(configFilePath)
			if err != nil {
				return "", err
			}

			_, err = configFile.WriteString(CradleConfigFileHeader)
			if err != nil {
				return "", err
			}

			return configFilePath, nil
		}

		return "", err
	}

	if configFileStat.IsDir() {
		return "", fmt.Errorf("")
	}

	return configFilePath, nil
}

func parseCradleConfigFile(configFilePath string) ([]types.CradleProject, error) {
	var cradleConfig cradleConfig

	configFile, err := os.ReadFile(configFilePath)
	if err != nil {
		return nil, err
	}

	err = yaml.Unmarshal(configFile, &cradleConfig)
	if err != nil {
		return nil, err
	}

	slices.SortFunc(cradleConfig.Projects, func(a, b types.CradleProject) int {
		return strings.Compare(a.Path, b.Path)
	})

	nameLookup := make(map[string]struct{})
	for i, project := range cradleConfig.Projects {
		parts := strings.Split(project.Path, string(os.PathSeparator))
		for j := len(parts) - 1; j >= 0; j-- {
			candidateName := strings.Join(parts[j:], "/")
			if candidateName == "" {
				continue
			}

			if _, exists := nameLookup[candidateName]; !exists {
				cradleConfig.Projects[i].UniqueNameFromPath = candidateName
				nameLookup[candidateName] = struct{}{}
				break
			}
		}
	}

	return cradleConfig.Projects, nil
}
